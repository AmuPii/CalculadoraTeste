#maneiras de moldar uma string

nome = "Herick Luis Ribeiro Raposo"

print (nome[0]) #primeira letra
print (nome[:5]) #primeiras 5 letras
print (nome[5:])#da 6 letra até o final
print (nome[10:16])#da 11 letra até a 16
print (nome[ : ])#toda a string
print (nome[::-1])#string invertida
print (nome[::2])#pula de 2 em 2
